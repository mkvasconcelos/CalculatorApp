function addition(a, b) {
    return a + b
}

function subtraction(a, b) {
    return a - b
}

function multiplication(a, b) {
    return a * b
}

function division(a, b) {
    return a / b
}

function square(a) {
    return a ** 2
}

function potentiation(a, b) {
    return a * b
}

function squareRoot(a) {
    return a ** 0.5
}
